public class Graus {
    public static void main(String[] args) {
        double temperaturaGrausCelsius = 23.4;
        double temperaturaFahrenheit = (temperaturaGrausCelsius * 1.8) + 32;

        System.out.println("A temperatura lá fora em Celsius é de: " + temperaturaGrausCelsius);
        System.out.println("A temperatura lá fora em Fahrenheit é de: " + temperaturaFahrenheit);
    }
}
